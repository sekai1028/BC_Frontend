export function pickRandomFromPool(pool: string[], exclude?: string): string {
  if (pool.length === 0) return ''
  const filtered = exclude ? pool.filter((s) => s !== exclude) : pool
  const arr = filtered.length ? filtered : pool
  return arr[Math.floor(Math.random() * arr.length)] ?? pool[0]
}

export function getRandomIntel(exclude?: string): string {
  return pickRandomFromPool(INTEL_ARRAY, exclude)
}

export function getRandomRunIntel(exclude?: string): string {
  return pickRandomFromPool(RUN_INTEL_ARRAY, exclude)
}
